void _log(String message) => stdout.writeln('[Ventio stress] $message');

void main() {
  test('synthetic launch stress test: catalog + sales + sync queue + backup roundtrip', () async {
    final productCount = _envInt('PRODUCTS', 1000);
    final saleCount = _envInt('SALES', 5000);
    final customerCount = _envInt('CUSTOMERS', 500);
    final supplierCount = _envInt('SUPPLIERS', 100);
    final maxItemsPerSale = max(1, _envInt('MAX_ITEMS_PER_SALE', 5));

    final store = await _readyStore();
    final random = Random(51);
    final totalWatch = Stopwatch()..start();

    _log('Seeding $supplierCount suppliers...');
    var watch = Stopwatch()..start();
    for (var i = 0; i < supplierCount; i++) {
      await store.addOrUpdateSupplier(_supplier(i));
    }
    _log('Suppliers done in ${watch.elapsedMilliseconds} ms');

    _log('Seeding $customerCount customers...');
    watch = Stopwatch()..start();
    for (var i = 0; i < customerCount; i++) {
      await store.addOrUpdateCustomer(_customer(i));
    }
    _log('Customers done in ${watch.elapsedMilliseconds} ms');

    _log('Seeding $productCount products...');
    watch = Stopwatch()..start();
    for (var i = 0; i < productCount; i++) {
      await store.addOrUpdateProduct(_product(i));
      if (i > 0 && i % 1000 == 0) _log('Products: $i/$productCount');
    }
    _log('Products done in ${watch.elapsedMilliseconds} ms');

    final products = store.products.toList();
    expect(products.length, productCount);

    final progressEvery = max(1, _envInt('PROGRESS_EVERY', 50));
    final slowMs = _envInt('SLOW_SALE_MS', 1000);
    final hardTimeoutSec = _envInt('SALE_TIMEOUT_SEC', 30);

    _log('Creating $saleCount sales... progressEvery=$progressEvery slowMs=$slowMs saleTimeout=${hardTimeoutSec}s');
    watch = Stopwatch()..start();
    var lastProgressAt = DateTime.now();
    var maxSaleMs = 0;
    var totalSaleMs = 0;
    var slowSales = 0;

    for (var i = 0; i < saleCount; i++) {
      final saleWatch = Stopwatch()..start();
      try {
        final itemCount = 1 + random.nextInt(maxItemsPerSale);
        final used = <String>{};
        final items = <SaleItem>[];

        while (items.length < itemCount) {
          final product = products[random.nextInt(products.length)];
          if (!used.add(product.id)) continue;
          final qty = 1 + random.nextInt(3);
          items.add(SaleItem(
            productId: product.id,
            productName: product.name,
            unitPrice: product.price,
            quantity: qty.toDouble(),
            unitName: product.unit,
            baseQuantity: qty.toDouble(),
            conversionToBase: 1,
            unitCost: product.usdCost,
          ));
        }

        await store.createSale(
          customerName: 'Stress Customer ${i % max(1, customerCount)}',
          items: items,
          paymentMethod: i.isEven ? 'Cash' : 'Card',
        ).timeout(Duration(seconds: hardTimeoutSec));
      } catch (error, stackTrace) {
        _log('FAILED at sale index=$i elapsedTotal=${watch.elapsed} salesCreated=${store.sales.length} products=${store.products.length} stockMovements=${store.stockMovements.length} pendingQueue=${store.pendingSyncQueueCount} pendingChanges=${store.pendingSyncChanges.length}');
        _log('ERROR: $error');
        stdout.writeln(stackTrace);
        rethrow;
      } finally {
        saleWatch.stop();
      }

      final ms = saleWatch.elapsedMilliseconds;
      totalSaleMs += ms;
      if (ms > maxSaleMs) maxSaleMs = ms;
      if (ms >= slowMs) {
        slowSales += 1;
        _log('SLOW sale=${i + 1}/$saleCount took=${ms}ms total=${watch.elapsed} sales=${store.sales.length} stockMovements=${store.stockMovements.length} pendingQueue=${store.pendingSyncQueueCount} pendingChanges=${store.pendingSyncChanges.length}');
      }

      if ((i + 1) % progressEvery == 0 || i == 0 || i + 1 == saleCount) {
        final avg = totalSaleMs / (i + 1);
        final now = DateTime.now();
        final sinceLast = now.difference(lastProgressAt).inMilliseconds;
        lastProgressAt = now;
        _log('Sales progress: ${i + 1}/$saleCount batchMs=$sinceLast avgSaleMs=${avg.toStringAsFixed(1)} maxSaleMs=$maxSaleMs slowSales=$slowSales total=${watch.elapsed} sales=${store.sales.length} stockMovements=${store.stockMovements.length} pendingQueue=${store.pendingSyncQueueCount} pendingChanges=${store.pendingSyncChanges.length}');
      }
    }
    _log('Sales done in ${watch.elapsedMilliseconds} ms avgSaleMs=${(totalSaleMs / max(1, saleCount)).toStringAsFixed(1)} maxSaleMs=$maxSaleMs slowSales=$slowSales');

    _log('Running assertions...');
    expect(store.sales.length, saleCount);
    expect(store.products.where((p) => p.stock < 0), isEmpty);
    expect(store.totalSalesAmount, greaterThan(0));
    expect(store.pendingSyncQueueCount, greaterThan(0));
    expect(store.pendingSyncChanges.length, greaterThanOrEqualTo(saleCount));

    _log('Exporting backup...');
    watch = Stopwatch()..start();
    final backup = store.exportBackupJson();
    final backupBytes = utf8.encode(backup).length;
    _log('Backup size: ${(backupBytes / 1024 / 1024).toStringAsFixed(2)} MB in ${watch.elapsedMilliseconds} ms');
    expect(store.validateBackupJson(backup).isValid, isTrue);

    _log('Resetting and importing backup...');
    watch = Stopwatch()..start();
    await store.resetBusinessData();
    await store.importBackupJson(backup);
    _log('Restore done in ${watch.elapsedMilliseconds} ms');

    expect(store.products.length, productCount);
    expect(store.sales.length, saleCount);
    expect(store.products.where((p) => p.stock < 0), isEmpty);

    totalWatch.stop();
    _log('DONE total=${totalWatch.elapsed} products=${store.products.length} sales=${store.sales.length} pendingQueue=${store.pendingSyncQueueCount} pendingChanges=${store.pendingSyncChanges.length}');
  }, timeout: const Timeout(Duration(minutes: 60)));
}
